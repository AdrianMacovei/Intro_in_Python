
age = input("What is your current age?\n")

# 354 days/year 52 weeks/year and 12 months/years
age_remain = (90 - int(age))
months = age_remain * 12
weeks = age_remain * 52
days = age_remain * 354
print (f"You have remaining {age_remain} years; {months} months; {weeks} weeks; {days} days until will passed away.\nBe carefull how you will spend this time! ")








